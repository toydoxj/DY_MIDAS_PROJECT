def interpolate_site_coeff(table: dict[str, list[float]], s_val: float, site_class: str) -> float:
    """[KDS 41 17 00:2022 표 4.3-1, 표 4.3-2] Fa/Fv 선형보간."""
    row = table.get(site_class)
    if not row:
        return 1.0
    return interpolate_piecewise(_S_POINTS, row, s_val)


def interpolate_cu(sd1: float) -> float:
    """[KDS 41 17 00:2022 표 7.2-1] Sd1 기반 Cu 선형보간."""
    points = [x for x, _ in CU_TABLE]
    values = [y for _, y in CU_TABLE]
    return interpolate_piecewise(points, values, sd1)


def classify_soil(bedrock_depth: float, shear_velocity: float) -> str:
    """[KDS 41 17 00:2022 표 4.2-4] 전단파속도/기반암깊이 기반 지반분류.

    Args:
        bedrock_depth: 기반암깊이 H (m)
        shear_velocity: 평균전단파속도 Vs (m/s)

    Returns:
        "S1" ~ "S5"
    """
    if bedrock_depth < 1:
        return "S1"
    if bedrock_depth <= 20:
        return "S2" if shear_velocity >= 260 else "S3"
    return "S4" if shear_velocity >= 180 else "S5"


def calc_cs(sds: float, sd1: float, r: float, ie: float, t: float) -> float:
    """[KDS 41 17 00:2022 §7.2] 등가정적 지진응답계수 Cs 계산.

    Cs = Sds / (R/Ie)
    상한: Sd1 / (T × R/Ie)
    하한: max(0.044 × Sds × Ie, 0.01)

    Args:
        sds: 설계스펙트럼 가속도 (단주기)
        sd1: 설계스펙트럼 가속도 (1초주기)
        r: 반응수정계수
        ie: 중요도계수
        t: 적용주기 (sec)

    Returns:
        지진응답계수 Cs
    """
    if r <= 0 or ie <= 0:
        return 0.0
    r_ie = r / ie
    cs = sds / r_ie

    # [KDS 41 17 00:2022 §7.2] 상한: Sd1 / (T × R/Ie)
    if t > 0:
        cs = min(cs, sd1 / (t * r_ie))

    # [KDS 41 17 00:2022 §7.2] 하한: max(0.044 × Sds × Ie, 0.01)
    cs = max(cs, max(0.044 * sds * ie, 0.01))

    return cs


def calc_seismic_category(sds: float, importance: str) -> str:
    """[KDS 41 17 00:2022 표 5.2-1] Sds 기반 내진설계범주 계산.

    Args:
        sds: 설계스펙트럼 가속도 (단주기)
        importance: 중요도 ("특", "(1)", "(2)", "(3)")

    Returns:
        "A" ~ "D"
    """
    if importance in ("특", "(1)"):
        if sds < 0.167:
            return "B"
        elif sds < 0.33:
            return "C"
        else:
            return "D"
    else:
        if sds < 0.167:
            return "A"
        elif sds < 0.33:
            return "B"
        elif sds < 0.50:
            return "C"
        else:
            return "D"


def calc_applied_period(
    hn: float,
    struct_code: int,
    sd1: float,
    t_eigen: float,
) -> float:
    """[KDS 41 17 00:2022 §7.2] 적용주기 T 계산.

    T = max(Ta, min(T_eigen, Cu × Ta))

    Args:
        hn: 건물높이 (m)
        struct_code: 구조형식 코드 (0=RC, 1=철골, 2=SRC, 3=조적)
        sd1: 설계스펙트럼 가속도 (1초주기) — Cu 계산용
        t_eigen: 고유치해석 주기 (sec)

    Returns:
        적용주기 T (sec)
    """
    ct, x = STRUCT_PERIOD_COEFF.get(struct_code, (0.0488, 0.75))
    ta = ct * (hn ** x)
    cu = interpolate_cu(sd1)
    cu_ta = cu * ta

    if t_eigen > 0:
        return max(ta, min(t_eigen, cu_ta))
    return ta
